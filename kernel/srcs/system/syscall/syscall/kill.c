// ! ||--------------------------------------------------------------------------------||

int kill(pid_t pid, int32_t sig) {
    if (pid == 0 || pid == 1)
        __THROW("Cannot kill PID %d (Protected)", -1, pid);
    signal(pid, sig);
    return (0);
}